public class USTVShowInfo {
    String tvName;
    String tvPoster;
    String tvTime;
    String tvSite;
    String tvState;

    public String getTvName() {
        return tvName;
    }

    public void setTvName(String tvName) {
        this.tvName = tvName;
    }

    public String getTvPoster() {
        return tvPoster;
    }

    public void setTvPoster(String tvPoster) {
        this.tvPoster = tvPoster;
    }

    public String getTvTime() {
        return tvTime;
    }

    public void setTvTime(String tvTime) {
        this.tvTime = tvTime;
    }

    public String getTvSite() {
        return tvSite;
    }

    public void setTvSite(String tvSite) {
        this.tvSite = tvSite;
    }

    public String getTvState() {
        return tvState;
    }

    public void setTvState(String tvState) {
        this.tvState = tvState;
    }
}
